CREATE DATABASE pokemon_za CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci; USE
    pokemon_za;
CREATE TABLE TYPE(
    type_id INT NOT NULL AUTO_INCREMENT,
    type_name VARCHAR(20) NOT NULL,
    PRIMARY KEY(type_id)
) ENGINE = INNODB; CREATE TABLE pokemon(
    pokemon_id INT NOT NULL AUTO_INCREMENT,
    pokemon_name VARCHAR(50) NOT NULL,
    pokemon_type_1 INT NOT NULL,
    pokemon_type_2 INT,
    pokemon_shiny_lock SMALLINT NOT NULL,
    pokemon_obtained SMALLINT NOT NULL,
    PRIMARY KEY(pokemon_id),
    CONSTRAINT fk_pokemon_type_1 FOREIGN KEY(pokemon_type_1) REFERENCES TYPE(type_id),
    CONSTRAINT fk_pokemon_type_2 FOREIGN KEY(pokemon_type_2) REFERENCES TYPE(type_id)
) ENGINE = InnoDB;

